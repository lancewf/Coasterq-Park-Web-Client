package com.coasterq.client.data;

public class Point
{
   // --------------------------------------------------------------------------
   // Private Data
   // --------------------------------------------------------------------------
  
   private double x;
   private double y;
   
   // --------------------------------------------------------------------------
   // Constructor
   // --------------------------------------------------------------------------
  
   public Point(double x, double y)
   {
      this.x = x;
      this.y = y;
   }
   
   // --------------------------------------------------------------------------
   // Public Members
   // --------------------------------------------------------------------------
  
   public double getX()
   {
      return x;
   }
   
   public double getY()
   {
      return y;
   }
}
